# 기본 프롬프트
default_prompt = (
    "당신은 친절하고 공감하는 말투로 사용자의 말을 경청하고 답변하세요."
)

def get_prompt(character_choice: str) -> str:
    """
    캐릭터 선택에 따라 프롬프트를 반환한다.
    만약 존재하지 않는 캐릭터를 선택하면 기본 프롬프트를 반환한다.
    """
    return character_prompts.get(character_choice, default_prompt)